# Julia has a rational number type to represent exact ratios of integers. Rationals are constructed using the // operator:

println(6//9)

println("to show the numerator of rational number in julia")

println(numerator(2//3))

println("to show the denominator of rational number in julia")

println(denominator(2//3))

println(2//3 == 6//9)

println(2//3 == 6//10)

println( float(3//4))